package model;

/**
 *
 * @author clump
 */
public class Math {
    
    Integer a, b;

    public Math(Integer a, Integer b) {
        this.a = a;
        this.b = b;
    }

    public Integer getA() {
        return a;
    }

    public void setA(Integer a) {
        this.a = a;
    }

    public Integer getB() {
        return b;
    }

    public void setB(Integer b) {
        this.b = b;
    }
    
    public Integer add(){
        return this.a + this.b;
    }
    
}
